import * as React from "react"

import { cn } from "@/lib/utils"

/**
 * Card component - INTENTIONALLY MODIFIED from standard shadcn
 *
 * Enhancement: Added `hover:shadow-md` for subtle elevation on hover.
 * This is a design system decision to provide consistent interactive feedback
 * across all card-based components. Many CMS components build on this base behavior.
 *
 * Standard shadcn Card does not include hover effects.
 */
const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-lg border bg-card text-card-foreground shadow-sm transition-all duration-300 hover:shadow-md",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

